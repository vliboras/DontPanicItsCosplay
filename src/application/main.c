/**
 * @file main.c
 *
 * @brief Application main loop
 *
 * @ingroup application
 */

/**********************************************************************************************************************
 * INCLUDES
 **********************************************************************************************************************/

#include "bsp.h"
#include "FreeRTOS.h"
#include "task.h"

/**********************************************************************************************************************
 * PUBLIC FUNCTIONS
 **********************************************************************************************************************/

void dummy_function(void *param)
{
    static uint32_t counter = 0;
    while (1)
    {
        counter++;
        vTaskDelay(100);
    }
}

/**
 * @brief  The application entry point.
 * @retval none
 */
int main(void)
{
    bsp_init();
    xTaskCreate(dummy_function, "Dummy", 128, NULL, 1, NULL);
    vTaskStartScheduler();
    while (1)
    {
    }
}

